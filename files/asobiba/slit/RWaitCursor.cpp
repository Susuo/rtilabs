#include "stdafx.h"
#include "RWaitCursor.h"

//////////////////////////////////////////////////////////////////////
// 構築/消滅
//////////////////////////////////////////////////////////////////////

RWaitCursor::RWaitCursor()
{
	this->OldCursor =  SetCursor(LoadCursor(NULL, IDC_WAIT));
}

RWaitCursor::~RWaitCursor()
{
	SetCursor(this->OldCursor);
}
